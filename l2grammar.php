	<h1> Lesson 2 </h1>
	<p> Welcome to your second lesson in Egyptian Hieroglyphs! There are a lot of things to learn at first, but it will get easier as you progress. This lesson will cover some grammar and some basic hieroglyphs. Let’s get started!
	</p>
	
	<h3> Words </h3>
	<p>Now onto some grammar points. Unlike English (though similar to Spanish, French, and many other languages), Egyptian marks grammatical gender on it’s nouns; There are masculine and feminine. Most nouns are either one or the other, through there are a couple of exceptions. You’ll mostly have to just memorize these, though place names are always feminine! An additional complication is with grammatical number. In English, when marking number, we can either have one of something(singular), or multiple of something(plural). For instance, I can have one paper or any number of multiple papers. Egyptian is similar, except when you have two of something. There is a special case called dual, which sits right in between singular and plural (3 or more). You won’t learn much about dual at the beginning, but it is important to point out. 
Additionally, there are no articles such as “a/an” or “the”. Something like the word “scribe” may be translated as “A scribe”, “The scribe”, or simply "scribe”.
</p>
	<h3> Sentences </h3>
	<p>Now that you know how writing is done and about the words you’ll use, it’s time to think about sentences. Sentences in Egyptian are likely very different from anything you may have seen before. First off, some sentences do not require a verb. For instance, the sentence “The man is in the house” would be translated to the effect of the “The man in the house”. These types of constructions are known as a non-verbal sentence. There are also verbal sentences such as the “The woman reads the texts”. These sentences contain a verb. Now, let’s learn how to make our own sentences.
We’re going to start with simple statements. “The woman kicks the ball” or “The man speaks to me” are great examples to work with. In English, you start with the subject (The woman or the man), you bring in a verb to say what the subject is or is doing, and then you have more information following it. English is what’s known as a SVO or Subject-Verb-Object language because that’s the order things come in. Egyptian is very different. In verbal sentences, Egyptian is VSO. It may be a little mind-bending, but our previous examples would be written as “Kicks the woman the ball” and “Speaks the man to me” and mean the exact same thing. 
In addition to being a VSO language, Egyptian has two other things that get added onto a sentence. Before even the verb in the language, you have to add a character to indicate what type of sentence you are saying. This character is known as a particle, and for simple sentences it is (INSERT). It is pronounced like “iw” or “(INSERT)”. After the objects at the end, you put all your adverbial modifiers on. You’ll only learn one this lesson, but you’ll eventually learn more. They always go at the end. With all this in mind, your basic statement sentence in Egyptian looks like:
Particle – Verb – Subject – Object – Adverbial.
Try making your own!
</p>

