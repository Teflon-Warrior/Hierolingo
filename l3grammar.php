	<h3> Declension </h3>
	
	<p>Our first topic is declension. Declension might be completely new to you as an English speaker, 
	through many other languages show heavy declension. Declension is the process of changing a word to show grammatical distinctions, 
	such as case, tense, number, and many other things. In English, only our pronouns really decline. 
	You would say “I saw him” rather than “I saw he”, or “It is mine” to indicate you own something, rather than “It is I”. 
	Using he insteam of him or him instead of he is due to declension.
	Egyptian, as talked about last lesson, has declensions for gender and number for nouns. 
	To show how words decline, we’ll be putting them in a cool table for you. </p>
